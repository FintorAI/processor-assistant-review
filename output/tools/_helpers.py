def _efolder_present(state: dict, doc_type: str) -> bool:
    """Return True if a document of doc_type exists in the eFolder (regardless of extraction).

    Checks efolder_documents directly — use this for presence checks instead of
    _doc() which returns an extracted field value (can be None even if doc exists).

    Usage:
        if not _efolder_present(state, "1003 URLA"):
            # flag as missing
    """
    entry = state.get("efolder_documents", {}).get(doc_type)
    if not entry:
        return False
    # Guard against empty/stub entries
    if isinstance(entry, dict):
        return bool(entry.get("copy_count", 0) or entry.get("attachment_id") or entry.get("extracted_fields"))
    return bool(entry)


def _efolder_bucket(state: dict, doc_type: str) -> str:
    """Return the actual Encompass eFolder bucket name used for this document type.

    Returns the canonical doc_type name as fallback if bucket info is not stored.

    Usage:
        bucket = _efolder_bucket(state, "1003 URLA")  # -> "1003"
        f"eFolder bucket: '{bucket}'"
    """
    entry = state.get("efolder_documents", {}).get(doc_type)
    if isinstance(entry, dict):
        return entry.get("encompass_bucket") or doc_type
    return doc_type


def _doc_all(state: dict, key: str) -> list:
    """Get all values for a doc field across multiple document copies.

    For multi-copy doc types (e.g., COC forms, purchase agreement addenda),
    returns the list of copy dicts from the ``copies`` key. Falls back to
    a single-element list wrapping the primary value if no copies exist.

    Usage:
        all_coc_changes = _doc_all(state, "coc_changes")
        for copy in all_coc_changes:
            print(copy["value"], copy["source_document"])
    """
    entry = state.get("doc_fields", {}).get(key, {})
    if not isinstance(entry, dict):
        return []
    if "copies" in entry and entry["copies"]:
        return entry["copies"]
    val = entry.get("value")
    if val is not None:
        return [{"value": val, "source_document": entry.get("source_document"), "confidence": entry.get("confidence"), "copy_index": 0}]
    return []


