def _id_copies(state: dict) -> dict:
    """Collect per-copy Government ID info from the Driver's License doc copies.

    Returns {copy_index: {"gov_id": ..., "gov_id_type": ..., "name": ...}}.
    """
    out: dict = {}

    def _ingest(key: str, field: str) -> None:
        for c in _doc_all(state, key):
            if not isinstance(c, dict):
                continue
            val = c.get("value")
            if val in (None, ""):
                continue
            out.setdefault(c.get("copy_index", 0), {}).setdefault(field, val)

    _ingest("dl_gov_id", "gov_id")
    _ingest("dl_gov_id_type", "gov_id_type")
    _ingest("dl_borrower_name", "name")
    _ingest("dl_name", "name")  # fallback name source
    return out


def _match_copy_to_person(records: dict, first, last):
    """Return the copy_index whose ID name best matches the given person, else None."""
    first_l = (first or "").strip().lower()
    last_l = (last or "").strip().lower()
    if not (first_l or last_l):
        return None
    best = None
    for ci, rec in records.items():
        name = (rec.get("name") or "").lower()
        if not name:
            continue
        score = (2 if last_l and last_l in name else 0) + (1 if first_l and first_l in name else 0)
        if score and (best is None or score > best[1]):
            best = (ci, score)
    return best[0] if best else None


def _blank(val) -> bool:
    """True when a LOS value is missing/empty (so it's safe to fill)."""
    return not (val and str(val).strip())


def _parse_date(val):
    """Best-effort parse of a date string to a date, or None."""
    if not val:
        return None
    for fmt in ("%m/%d/%Y", "%Y-%m-%d", "%m-%d-%Y", "%m/%d/%y"):
        try:
            return datetime.strptime(str(val).strip(), fmt).date()
        except ValueError:
            continue
    return None


def _surname_on(surname, text) -> bool:
    """True when the borrower surname appears in a name string as whole token(s).

    Conservative on purpose: only used to raise a *mismatch* flag when the
    borrower's last name is entirely absent from a document name. Compares
    tokenized name parts rather than a raw substring so a short surname does not
    match inside an unrelated name (e.g. 'Le' inside 'Leonard'); a multi-token
    surname must have every token present as a whole token.
    """
    s_tokens = [tok for tok in re.split(r"[^a-z0-9]+", (surname or "").lower()) if tok]
    t_tokens = {tok for tok in re.split(r"[^a-z0-9]+", (text or "").lower()) if tok}
    if not s_tokens or not t_tokens:
        return False
    return all(tok in t_tokens for tok in s_tokens)


def _digits(val) -> str:
    """Digits-only string (used to compare SSNs without formatting noise)."""
    return re.sub(r"\D", "", str(val or ""))


def _norm_addr(val) -> str:
    """Lowercase, punctuation-stripped, single-spaced address for loose compare."""
    return re.sub(r"[^a-z0-9]+", " ", str(val or "").lower()).strip()


def _norm_name(val) -> str:
    """Lowercase, punctuation-stripped, single-spaced name for loose compare."""
    return re.sub(r"[^a-z0-9]+", " ", str(val or "").lower()).strip()


def _occ_is_investment(occ) -> bool:
    """True for investment / non-owner-occupied LOS occupancy values."""
    o = str(occ or "").lower()
    return any(k in o for k in ("investment", "investor", "non-owner", "nonowner", "non owner"))


def _occ_is_primary(occ) -> bool:
    """True only for an unambiguous owner-occupied / primary occupancy.

    Mutually exclusive with :func:`_occ_is_investment` — a value like
    'Non-Owner Occupied' contains 'owner' but must NOT count as primary."""
    o = str(occ or "").lower()
    if not o or _occ_is_investment(o) or any(k in o for k in ("second", "vacation")):
        return False
    return "primary" in o or "owner" in o


def _split_akas(raw) -> list:
    """Split a credit-report AKA blob into individual alternate names.

    Handles common delimiters and the 'AKA' / 'A/K/A' / 'FKA' markers; drops
    empty fragments."""
    parts = re.split(r"\s*(?:;|,|/|\||\band\b|\ba/?k/?a\b|\bf/?k/?a\b|\baka\b|\bfka\b)\s*",
                     str(raw or ""), flags=re.IGNORECASE)
    return [p.strip() for p in parts if p and p.strip()]


def _addr_match(a, b) -> bool:
    """Loose address equality: same house number AND street-name token overlap.

    Returns True when we cannot meaningfully compare (missing data) so a
    mismatch is only raised on a genuine house-number/street disagreement."""
    na, nb = _norm_addr(a), _norm_addr(b)
    if not na or not nb:
        return True
    ta, tb = na.split(), nb.split()
    an = ta[0] if ta else ""
    bn = tb[0] if tb else ""
    if an.isdigit() and bn.isdigit() and an != bn:
        return False
    return len(set(ta) & set(tb)) >= 2


def _appraisal_address(state: dict) -> Optional[str]:
    """Appraisal-specific subject address.

    Prefers the collision-free ``appraisal_property_address`` (lowercase
    ``appraisal_report`` schema). Falls back to a ``property_address`` copy whose
    provenance is the appraisal — that key is shared with the Title Report, so we
    only accept it when ``source_document`` names the appraisal."""
    v = _doc(state, "appraisal_property_address")
    if v:
        return v
    for c in _doc_all(state, "property_address"):
        if "appraisal" in (c.get("source_document") or "").lower() and c.get("value"):
            return c.get("value")
    return None


def _sourced_property_address(state: dict, source_hint: str) -> Optional[str]:
    """A ``property_address`` copy whose provenance matches ``source_hint``.

    The ``property_address`` key is shared across the Title Report, Lock
    Confirmation, CPL, etc., so we only accept the copy whose ``source_document``
    names the document we want (e.g. 'title')."""
    for c in _doc_all(state, "property_address"):
        if source_hint in (c.get("source_document") or "").lower() and c.get("value"):
            return c.get("value")
    return None


def _norm_gov_id_type(raw) -> Optional[str]:
    """Normalize an OCR'd Government ID type to a canonical Encompass value.

    The source is always the Driver's License doc, so license-like variants map
    to "DL"; any other extracted type is preserved as-is."""
    s = str(raw or "").strip()
    if not s:
        return None
    low = s.lower()
    if "driver" in low or low in ("dl", "dln", "d/l", "dl#"):
        return "DL"
    return s


def _write_government_id(state, loan_id, flags, b_first, b_last, c_first, c_last, has_co) -> None:
    """Populate Government ID + Type for borrower (5053/5055) and co-borrower
    (5054/5056) from the Driver's License doc content, mapping each license copy
    to the correct person by name (falling back to copy order)."""
    records = _id_copies(state)
    if not records:
        return

    order = sorted(records)
    b_ci = _match_copy_to_person(records, b_first, b_last)
    if b_ci is None and order:
        b_ci = order[0]

    c_ci = None
    if has_co:
        c_ci = _match_copy_to_person(records, c_first, c_last)
        if c_ci is None or c_ci == b_ci:
            rest = [ci for ci in order if ci != b_ci]
            c_ci = rest[0] if rest else None

    # field id → LOS state key, so we can honor write-only-if-blank and not
    # clobber a manually-corrected Government ID / Type on every run.
    _gov_los = {"5053": "borrower_gov_id", "5054": "coborrower_gov_id",
                "5055": "borrower_gov_id_type", "5056": "coborrower_gov_id_type"}

    def _write_person(ci, id_field, type_field, label):
        if ci is None:
            return
        rec = records.get(ci, {})
        updates = {}
        # 5053/5054 — Government ID number, write only when Encompass is blank.
        if rec.get("gov_id") and _blank(_los(state, _gov_los[id_field])):
            updates[id_field] = rec["gov_id"]
        # 5055/5056 — normalize OCR type to the canonical "DL" (source is the
        # Driver's License doc); write only when blank.
        norm_type = _norm_gov_id_type(rec.get("gov_id_type"))
        if norm_type and _blank(_los(state, _gov_los[type_field])):
            updates[type_field] = norm_type
        if updates:
            _write_fields(loan_id=loan_id, updates=updates, substep="2.1",
                          flags=flags, state=state,
                          labels={id_field: f"{label} Government ID",
                                  type_field: f"{label} Government ID Type"})

    _write_person(b_ci, "5053", "5055", "Borrower")
    if has_co:
        _write_person(c_ci, "5054", "5056", "Co-Borrower")


