_ALL_TOOLS_REF = None


def resolve_tools_for_step(state: dict) -> list:
    """Return only the tools relevant to the current step.

    This resolver:
    1. Excludes tools from config-skipped substeps (dev mode)
    2. Excludes tools from dynamically-skipped substeps (runtime state)
    3. Returns only SKIP_STEP_TOOLS when the entire step is skipped
    4. Returns empty list when workflow is COMPLETED
    """
    global _ALL_TOOLS_REF

    if _ALL_TOOLS_REF is None:
        return []

    current_step = get_current_step_from_state(state)

    if current_step == "COMPLETED":
        return []

    # If the whole step is skipped, only allow write_todo and status tools
    if is_step_skipped(current_step):
        return [t for t in _ALL_TOOLS_REF
                if getattr(t, "name", getattr(t, "__name__", "")) in SKIP_STEP_TOOLS]

    # Build set of dynamically-skipped substep IDs from state
    dynamic_skipping = set(state.get("dynamic_skipping", []) or [])

    # Get allowed tool names, excluding both config-skipped and dynamically-skipped substeps
    allowed_names = get_step_tools_excluding_skipped(
        current_step, extra_skip_substeps=dynamic_skipping
    )

    return [t for t in _ALL_TOOLS_REF
            if getattr(t, "name", getattr(t, "__name__", "")) in allowed_names]


def resolve_plan_for_step(state: dict) -> str | None:
    """Return the plan for the current step."""
    current_step = get_current_step_from_state(state)

    if current_step == "COMPLETED":
        return None
    if is_step_skipped(current_step):
        return None

    try:
        return load_plan_content(current_step)
    except Exception as e:
        logger.warning(f"[PLAN_RESOLVER] Could not load plan for {current_step}: {e}")
        return None


