def merge_dicts(existing: dict | None, new: dict | None) -> dict:
    """Deep merge two dictionaries, preserving nested structures."""
    if existing is None:
        return new or {}
    if new is None:
        return existing
    result = {**existing}
    for key, value in new.items():
        if key in result and isinstance(result[key], dict) and isinstance(value, dict):
            result[key] = merge_dicts(result[key], value)
        else:
            result[key] = value
    return result


def last_value_reducer(existing: Any | None, new: Any | None) -> Any | None:
    """Take the new value, falling back to existing if new is None."""
    return new if new is not None else existing


def dedupe_flags(existing: list | None, new: list | None) -> list:
    """Merge flags lists with deduplication by (substep, title).

    If the same (substep, title) appears again and the new entry is resolved
    while the stored entry is not, the new entry replaces the old one so
    follow-up tools can clear warnings raised in earlier steps.
    """
    all_flags = (existing or []) + (new or [])
    order: list[tuple[str, str]] = []
    by_key: dict[tuple[str, str], dict] = {}
    for flag in all_flags:
        if not isinstance(flag, dict):
            continue
        key = (flag.get("substep", ""), flag.get("title", ""))
        if "resolved" not in flag:
            flag["resolved"] = False
        if "timestamp" not in flag:
            flag["timestamp"] = datetime.now(timezone.utc).isoformat()
        flag.pop("hard_stop", None)

        if key not in by_key:
            by_key[key] = flag
            order.append(key)
        else:
            prev = by_key[key]
            if not prev.get("resolved") and flag.get("resolved"):
                by_key[key] = flag
    return [by_key[k] for k in order]


def truncate_messages(existing: list | None, new: list | None) -> list:
    """Merge messages with truncation of large tool results."""
    merged = add_messages(existing, new)
    truncated = []
    for msg in merged:
        if isinstance(msg, ToolMessage):
            content = msg.content
            if isinstance(content, str) and len(content) > MAX_TOOL_RESULT_SIZE:
                truncated_content = content[:MAX_TOOL_RESULT_SIZE] + "\n... [TRUNCATED]"
                truncated.append(ToolMessage(
                    content=truncated_content,
                    tool_call_id=msg.tool_call_id,
                    name=msg.name if hasattr(msg, "name") else None,
                ))
            else:
                truncated.append(msg)
        else:
            truncated.append(msg)
    return truncated


